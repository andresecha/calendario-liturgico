# en la cristiandad desde el año 325 d.C. (Concilio de Nicea) hasta 1582
# (reforma gregoriana del Papa Gregorio XIII).
#
# NOTA HISTÓRICA:
# El año 1582 es el último año del calendario juliano usado en este
# diccionario porque en octubre de 1582 se implementó la reforma gregoriana.
# Después de ese año, el cálculo de Pascua usa el algoritmo gregoriano.
